export async function getBlogs() {
  const res = await fetch("http://3.143.248.82/blogs", {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  });

  if (!res.ok) {
    throw new Error(`Failed to fetch blogs: ${res.status} ${res.statusText}`);
  }

  const data = await res.json();
  return data;
}

