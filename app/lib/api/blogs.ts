export async function createBlogPost({
  title,
  body,
  published,
  blogger_id,
}: {
  title: string;
  body: string;
  published: boolean;
  blogger_id: string;
}) {
  const res = await fetch(`http://3.143.248.82/blogs/post-blog`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${localStorage.getItem("token")}`,
    },
    body: JSON.stringify({
      title,
      body,
      published,
      blogger_id,
      // slug will be auto-generated by the backend from the title
    }),
  });

  if (!res.ok) {
    // Try to get the error message from the response
    let errorMessage = `Failed to create blog post: ${res.status} ${res.statusText}`;
    try {
      const errorData = await res.json();
      errorMessage += `\nServer error: ${JSON.stringify(errorData)}`;
    } catch {
      // If response is not JSON, try to get text
      const errorText = await res.text();
      if (errorText) {
        errorMessage += `\nServer response: ${errorText}`;
      }
    }
    throw new Error(errorMessage);
  }

  const data = await res.json();
  return data;
}

